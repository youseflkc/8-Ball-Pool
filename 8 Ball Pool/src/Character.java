/**Character class
 * 
 */
public class Character {
	String name;
	boolean turn;
	int points;
	boolean striped;
	
	public Character(String name, boolean turn, int points,boolean striped) {
		super();
		this.name = name;
		this.turn = turn;
		this.points = points;
		this.striped=striped;
		
	}
	
}
